CREATE OR REPLACE VIEW app_vw_shift_attendance AS
SELECT
  TRUNC(login_at) AS attendance_date,
  shift_number,
  user_id,
  username,
  MIN(login_at)  AS first_login_at,
  MAX(logout_at) AS last_logout_at,
  COUNT(*)       AS session_count
FROM app_shift_sessions
WHERE status = 'CLOSED' OR (status = 'ACTIVE' AND logout_at IS NULL)
GROUP BY TRUNC(login_at), shift_number, user_id, username;

