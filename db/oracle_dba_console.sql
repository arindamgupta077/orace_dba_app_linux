  -- app_backup_status_checks
  drop_constraint_if_exists('app_backup_status_checks', 'CK_BKCHK_SHIFT');
  add_constraint_ignore_exists(q'[ALTER TABLE app_backup_status_checks ADD CONSTRAINT ck_bkchk_shift CHECK (shift_number IN (1,2,3,4))]');
