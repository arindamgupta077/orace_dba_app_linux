DECLARE
  PROCEDURE drop_constraint_if_exists(p_table VARCHAR2, p_constraint VARCHAR2) IS
  BEGIN
    EXECUTE IMMEDIATE 'ALTER TABLE ' || p_table || ' DROP CONSTRAINT ' || p_constraint;
  EXCEPTION
    WHEN OTHERS THEN
      IF SQLCODE != -2443 THEN
        RAISE;
      END IF;
  END;
  PROCEDURE add_constraint_ignore_exists(p_sql VARCHAR2) IS
  BEGIN
    EXECUTE IMMEDIATE p_sql;
  EXCEPTION
    WHEN OTHERS THEN
      IF SQLCODE NOT IN (-2260, -2261, -2264, -2275) THEN
        RAISE;
      END IF;
  END;
BEGIN
  -- app_shift_sessions
  drop_constraint_if_exists('app_shift_sessions', 'CK_SHIFT_SESSION_SHIFT');
  add_constraint_ignore_exists(q'[ALTER TABLE app_shift_sessions ADD CONSTRAINT ck_shift_session_shift CHECK (shift_number IN (1,2,3,4))]');

  -- app_handovers
  drop_constraint_if_exists('app_handovers', 'CK_HANDOVER_SHIFT');
  add_constraint_ignore_exists(q'[ALTER TABLE app_handovers ADD CONSTRAINT ck_handover_shift CHECK (shift_number IN (1,2,3,4))]');

  -- app_db_status_checks
  drop_constraint_if_exists('app_db_status_checks', 'CK_DBCHK_SHIFT');
