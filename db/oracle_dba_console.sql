-- Tables:
--   app_shift_sessions        DBA shift login/logout records
--   app_handovers             Shift handover notes + acknowledgement
--   app_backup_template       Master backup definitions (app_admin maintained)
--   app_db_status_checks      Per-DB per-shift database availability checks
--   app_backup_status_checks  Per-backup per-shift backup status checks
--   app_webhook_logs          n8n webhook dispatch audit log
--
-- Views:
--   app_vw_current_shift      Active shift sessions joined to users
--   app_vw_shift_attendance   Daily/monthly attendance rollup
--
-- Run as the application schema owner that owns APP_USERS and DATABASE_INVENTORY.
-- Idempotent: safe to re-run.
--------------------------------------------------------------------------------

DECLARE
  v_count NUMBER;
  PROCEDURE drop_constraint_if_exists(p_table VARCHAR2, p_constraint VARCHAR2) IS
  BEGIN
    EXECUTE IMMEDIATE 'ALTER TABLE ' || p_table || ' DROP CONSTRAINT ' || p_constraint;
  EXCEPTION
    WHEN OTHERS THEN
      IF SQLCODE != -2443 THEN
        RAISE;
      END IF;
  END;
  PROCEDURE drop_index_if_exists(p_index VARCHAR2) IS
  BEGIN
    EXECUTE IMMEDIATE 'DROP INDEX ' || p_index;
  EXCEPTION
    WHEN OTHERS THEN
      IF SQLCODE != -1418 THEN
        RAISE;
      END IF;
  END;
BEGIN
  NULL;
END;
